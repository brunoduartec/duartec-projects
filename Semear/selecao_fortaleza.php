<h2 align="center">Inscrição para o Processo de Seleção 2013 – Instituto Semear Fortaleza</h2>
<p align="center">&nbsp;</p>
<p >Este Processo de Seleção está aberto aos candidatos de Fortaleza. <br />
  <br />
Somente são aptos a participar deste Processo Seletivo os candidatos que atendem as duas condições especificadas abaixo :<br>
</p>
<p > 	<strong>- Estar em qualquer Universidade (pública ou particular com prouni) e cursar o primeiro ou o segundo ano durante 2013; </strong><strong><br />
  - Ter estudado em escola pública (ou particular com bolsa integral) durante o ensino médio. </strong><br>
  <br />
  Este Processo Seletivo acontecerá entre os dias<strong> 8 e 23 de março</strong>, sendo obrigatória a presença conforme a  aprovação nas etapas do processo. Os que obtiverem aprovação em todas as etapas deverão comparecer ao Primeiro Encontro dos Bolsistas, com data a ser definida. <br />
  <br />
  
 A inscrição no Processo Seletivo poderá ser feita do dia <strong>15 de fevereiro a 28 de fevereiro</strong>, contudo o candidato deverá estar apto já no dia 8 de março a comparecer a nossa apresentação institucional, em local a ser divulgado.<br />
 
  É importante que os candidatos fiquem atentos ao <a href = "edital processo seletivo 2013 - fortaleza.doc" target='_blank'>regulamento</a> do nosso processo seletivo. Nosso regulamento contém mais informações sobre as etapas do processo seletivo. <br />
  <br />
É importante ficar atento à <a href ="Documentação - IS Fortaleza.docx" target='_blank'> documentação</a> que deverá ser apresentada no dia da dinâmica de grupo. Caso seja necessário (de acordo com as instruções do arquivo acima), deverá ser preenchida a declarações a seguir: <a href = "Declaracao_de_Trabalho_Informal.pdf" target='_blank'>Declaração de Trabalho Informal</a>.  </p>
<p >Além disso, o candidato deve preencher a ficha abaixo. <br />
<strong>Atenção: </strong>os próximos passos para o Processo Seletivo serão divulgados através do e-mail cadastrado no Formulário abaixo. Caso você não receba nenhum comunicado até o dia <strong>6 de março</strong>, entre em contato imediatamente através do e-mail: <strong>contatofortaleza@isemear.com.br</strong> .</p>
