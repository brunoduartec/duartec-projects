&nbsp;&nbsp;<h1>Encontro de Carnaval Folia de Luz</h1><br>
&nbsp;&nbsp;Regras:
<hr color="#CCFFFF" size="">
<ul>
<li>
Evitaremos transtornos desnecessários se todos respeitarem as regras básicas:
</li><li>
Não será permitido "ficar" e namorar durante o encontro;
</li><li>
Não será permitido o uso de qualquer tipo de produto fumígero (cigarro, charuto, etc) ou drogas de qualquer natureza no recinto do evento;
</li><li>
Não será permitido bebida alcoólica;
</li><li>
Não será permitido o uso de equipamentos eletrônicos durante as atividades e plenário (mp3, iPod, celulares...);
</li><li>
Não será permitido porte de armas de qualquer espécie;
</li><li>
Respeitar horários (acordar, dormir, refeições...);
</li><li>
Comunicar a organização qualquer problema de saúde durante o encontro;
</li><li>
Os participantes não poderão sair/voltar do encontro sem autorização da organização do evento;
</li><li>
O evento será no verão, mas as roupas devem estar de acordo com o ambiente religioso e fraterno (não usar shorts, mini-blusas, roupas transparentes, decotadas, etc).
</li>
</ul>
<br>
<div class="black">
&nbsp;&nbsp;Sempre vale ressaltar que se trata de um EVENTO RELIGIOSO, portanto bom senso com as palavras, gestos, vestuário, respeitando o ambiente e o colega.<br>
&nbsp;&nbsp;Pedimos a colaboração de todos os participantes para juntos mantermos um ambiente agradável e fraterno, pois o encontro é de amigos e afins, e os que não estiverem com esse propósito não estarão no local adequado.<br><br>
"Tudo me é permitido, porém nem tudo me convém." (Paulo de Tarso) 
